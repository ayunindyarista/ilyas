    <p style="text-align: center;"><strong>PASAL 11</strong><br /><strong>KEADAAN KAHAR/MEMAKSA</strong></p>
    <ol style="text-align: justify;">
        <li><strong>PARA PIHAK</strong> dibebaskan dari tanggung jawab atas keterlambatan atau kegagalan dalam memenuhi kewajiban yang dimaksud dalam <strong>Perjanjian Pendanaan Penelitian </strong> disebabkan atau diakibatkan oleh peristiwa atau kejadian diluar kekuasaan <strong>PARA PIHAK</strong> yang dapat digolongkan sebagai keadaan memaksa (force majeure).</li><br><br>
        <li>Peristiwa atau kejadian yang dapat digolongkan keadaan memaksa (force majeure) dalam <strong>Perjanjian Pendanaan Penelitian</strong> ini adalah bencana alam, wabah penyakit, kebakaran, perang, blokade, peledakan, sabotase, revolusi, pemberontakan, huru-hara, serta adanya tindakan pemerintah dalam bidang ekonomi dan moneter yang secara nyata berpengaruh terhadap pelaksanaan <strong>Perjanjian Pendanaan Penelitian</strong> ini.</li><br><br>
        <li>Apabila terjadi keadaan memaksa (force majeure) maka pihak yang mengalami wajib memberitahukan kepada pihak lainnya secara tertulis, selambat-lambatnya dalam waktu 7 (tujuh) hari kerja sejak terjadinya keadaan memaksa (force majeure), disertai dengan bukti-bukti yang sah dari pihak yang berwajib, dan <strong>PARA PIHAK</strong> dengan itikad baik akan segera membicarakan penyelesaiannya.</li>
    </ol>

